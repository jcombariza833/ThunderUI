import Foundation
import SwiftUI
@_exported import ThunderUIKit

/// A protocol defining the visual styling for a button, including colors, typography, and interaction states.
public typealias ButtonScheme = ThunderUIKit.ButtonScheme

/// A protocol defining the styling for a text field, including background, border, placeholder, and font properties.
public typealias TextFieldScheme = ThunderUIKit.TextFieldScheme

/// A protocol defining the visual styling for an icon component, focusing on background states.
public typealias IconScheme = ThunderUIKit.IconScheme

/// A protocol defining the styling for a snackbar component, including colors, typography, and shadow.
public typealias SnackbarScheme = ThunderUIKit.SnackbarScheme

/// A protocol defining the visual styling for a toggle component, including colors and font.
public typealias ToggleScheme = ThunderUIKit.ToggleScheme

