import Foundation
import SwiftUI
@_exported import ThunderUIKit

/// A text scheme for H1 headers that adapts to the current color scheme (light or dark).
public typealias TUIH1TextScheme = ThunderUIKit.TUIH1TextScheme

/// A text scheme for H2 headers that adapts to the current color scheme (light or dark).
public typealias TUIH2TextScheme = ThunderUIKit.TUIH2TextScheme

/// A text scheme for H3 headers that adapts to the current color scheme (light or dark).
public typealias TUIH3TextScheme = ThunderUIKit.TUIH3TextScheme

/// A text scheme for H4 headers that adapts to the current color scheme (light or dark).
public typealias TUIH4TextScheme = ThunderUIKit.TUIH4TextScheme

/// A text scheme for H5 headers that adapts to the current color scheme (light or dark).
public typealias TUIH5TextScheme = ThunderUIKit.TUIH5TextScheme

/// A text scheme for H6 headers that adapts to the current color scheme (light or dark).
public typealias TUIH6TextScheme = ThunderUIKit.TUIH6TextScheme

/// A text scheme for Subtitle1 text, adapting to the active color scheme (light or dark).
public typealias TUIS1TextScheme = ThunderUIKit.TUIS1TextScheme

/// A text scheme for Subtitle2 text, adapting to the active color scheme (light or dark).
public typealias TUIS2TextScheme = ThunderUIKit.TUIS2TextScheme

/// A text scheme for Body1 text, styled with medium-opacity foreground to enhance readability in both color schemes.
public typealias TUIB1TextScheme = ThunderUIKit.TUIB1TextScheme

/// A text scheme for Body2 text, styled with medium-opacity foreground to enhance readability in both color schemes.
public typealias TUIB2TextScheme = ThunderUIKit.TUIB2TextScheme

/// A text scheme for caption-style text, using reduced opacity for subdued, supportive content.
public typealias TUICTextScheme = ThunderUIKit.TUICTextScheme

/// A text scheme for overline text, styled with low-opacity foreground for subtle labeling or annotations.
public typealias TUIOTextScheme = ThunderUIKit.TUIOTextScheme
